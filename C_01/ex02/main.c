#include <unistd.h>
#include "ft_swap.c"

void	ft_swap(int *a, int *b);

int	main(void)
{
	int	i;
	int	j;

	i = 42;
	j = 43;
	write(1, &i, 1);
	write(1, &j, 1);
	ft_swap(&i, &j);
	write(1, &i, 1);
	write(1, &j, 1);
}
